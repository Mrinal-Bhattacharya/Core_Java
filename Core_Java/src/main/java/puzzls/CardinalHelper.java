package puzzls;

public class CardinalHelper {
    
    public boolean isEastFacing(final char cuurentFace) {
        return cuurentFace == 'E';
    }

    public boolean isSouthFacing(final char cuurentFace) {
        return cuurentFace == 'S';
    }

    public boolean isNorthFacing(final char cuurentFace) {
        return cuurentFace == 'N';
    }
}
